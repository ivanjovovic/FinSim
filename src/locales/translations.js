// Helper function to get translated text
export const t = (language, key) => {
  const keys = key.split('.');
  let result = translations[language];
  
  for (const k of keys) {
    if (result && typeof result === 'object') {
      result = result[k];
    } else {
      return key; // Return key if translation not found
    }
  }
  
  return result || key;
};
